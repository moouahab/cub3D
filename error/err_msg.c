bool	error_msg(char *msg, void *display)
{
	if (msg != NULL)
	{
		printf("\n---------------------------------------------\n"
				"- %s\n"
				"---------------------------------------------\n\n",
				msg);
	}
	if (display != NULL)
	{
		mlx_destroy_display(display);
		free(display);
	}
	return (false);
}

